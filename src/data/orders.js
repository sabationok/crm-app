const countRowData = data => {
  let newData = { ...data };

  if (data?.price && data?.quantity) {
    newData.summ = data.price * data.quantity;
  }

  if (data?.sale && newData?.summ) {
    newData.saleSumm = (newData.summ * (100 - data.sale)) / 100;
  }

  return newData;
};
